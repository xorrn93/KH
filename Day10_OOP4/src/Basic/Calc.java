package Basic;

public class Calc {
	public int plus(int num1, int num2) {
		return num1+num2;
	}
	public static int minus(int num1, int num2) {
		return num1-num2;
	}
	public int mply(int num1, int num2) {
		return num1*num2;
	}
	public static float divide(float num1, float num2) {
		return num1/num2;
	}
}
